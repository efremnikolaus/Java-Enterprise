package MentorLessons.lesson01;

public class StaffMember {
    private String name;
    private String post;
    private String email;
    private int phone;
    private int age;

    public StaffMember(String name, String post, String email, int phone, int age) {
        this.name = name;
        this.post = post;
        this.email = email;
        this.phone = phone;
        this.age = age;
    }
}